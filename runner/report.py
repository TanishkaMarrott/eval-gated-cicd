"""Generates the GitHub PR comment report."""

from datetime import datetime


PASS_EMOJI = "✅"
FAIL_EMOJI = "❌"
WARN_EMOJI = "⚠️"


def build_pr_comment(eval_result: dict, gate_result: dict, sample_pct: float) -> str:
    total = eval_result["total_cases"]
    overall = gate_result["gate"]
    overall_icon = PASS_EMOJI if overall == "pass" else FAIL_EMOJI

    lines = [
        f"## {overall_icon} Eval Gate — {overall.upper()}",
        f"",
        f"Ran **{total} cases** ({int(sample_pct * 100)}% sample) · {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}",
        f"",
        f"### Per-axis results",
        f"",
        f"| Axis | Baseline | Corrected | CI Lower | Delta | Gate |",
        f"|------|----------|-----------|----------|-------|------|",
    ]

    for axis, data in eval_result["per_axis"].items():
        gate_data = gate_result["axes"].get(axis, {})
        gate_status = gate_data.get("gate", "pass")
        icon = PASS_EMOJI if gate_status == "pass" else FAIL_EMOJI
        baseline = gate_data.get("baseline", "—")
        baseline_str = f"{baseline:.2%}" if isinstance(baseline, float) else baseline
        corrected = data["corrected_pass_rate"]
        ci_lower = data["ci_lower"]
        delta = gate_data.get("delta", "—")
        delta_str = f"{delta:+.2%}" if isinstance(delta, float) else delta
        method = data.get("method", "raw")
        method_note = "" if method == "judgy" else f" ({WARN_EMOJI}raw)"
        lines.append(
            f"| {axis} | {baseline_str} | {corrected:.2%}{method_note} | {ci_lower:.2%} | {delta_str} | {icon} |"
        )

    lines += ["", "### Failing cases"]

    any_fail = False
    for axis, data in eval_result["per_axis"].items():
        gate_data = gate_result["axes"].get(axis, {})
        if gate_data.get("gate") == "fail":
            any_fail = True
            lines.append(f"")
            lines.append(f"**{axis}** — regressed below baseline")
            for case in data["cases"]:
                if not case["pass"]:
                    lines.append(f"- `{case['id']}`: {case['reason']}")
                    lines.append(f"  > Q: {case['question'][:80]}")

    if not any_fail:
        lines.append(f"")
        lines.append(f"No regressions detected.")

    if gate_result.get("reason"):
        lines += ["", f"_{gate_result['reason']}_"]

    lines += [
        "",
        "---",
        "_Generated by [eval-gated-cicd](https://github.com/TanishkaMarrott/eval-gated-cicd)_",
    ]

    return "\n".join(lines)
